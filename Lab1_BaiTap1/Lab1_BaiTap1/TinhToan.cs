using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Lab1_BaiTap1
{
	internal class TinhToan
	{
		
		public static double CongHaiSo(int a, int b)
		{
			return a + b;
		}

		public static double TruHaiSo(int a, int b)
		{
			return a - b;
		}
		public static double NhanHaiSo(int a, int b)
		{
			return a* b;
		}
		public static double ChiaHaiSo(int a, int b)
		{
			return (double)a / b;
		}
	}
}
